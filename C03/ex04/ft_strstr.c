char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (*to_find == '\0')
		return (str);
	while (str[i])
	{
		j = 0;
		if (str[i] == to_find[j])
		{
			while (str[i + j] == to_find[j] || to_find[j] == '\0')
			{
				if (to_find[j] == '\0')
					return (&str[i]);
				j++;
			}
		}
		i++;
	}
	return (0);
}
